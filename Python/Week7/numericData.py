#variables
#Variables are used to store data in a program.
#Valid variable names can contain letters, numbers, and underscores, but cannot start with a number.
# Variable names are case-sensitive, meaning that `myVariable` and `myvariable` are considered different variables.
my_variable = 10
my_variable2 = 20
print(my_variable)
print(my_variable2)
# You can perform arithmetic operations with variables
sum_variable = my_variable + my_variable2
print(f"Sum of my_variable and my_variable2: {sum_variable}")